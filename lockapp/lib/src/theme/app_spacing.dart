class AppSpacing {
  AppSpacing._();

  // Base spacing unit
  static const double baseUnit = 8.0;

  // Padding values
  static const double xs = baseUnit * 0.5; // 4.0
  static const double sm = baseUnit * 1.0; // 8.0
  static const double md = baseUnit * 2.0; // 16.0
  static const double lg = baseUnit * 3.0; // 24.0
  static const double xl = baseUnit * 4.0; // 32.0
  static const double xxl = baseUnit * 5.0; // 40.0
  static const double xxxl = baseUnit * 6.0; // 48.0

  // Margin values
  static const double marginXs = xs;
  static const double marginSm = sm;
  static const double marginMd = md;
  static const double marginLg = lg;
  static const double marginXl = xl;
  static const double marginXxl = xxl;
  static const double marginXxxl = xxxl;

  // Common spacing values
  static const double zero = 0.0;
  static const double tiny = 2.0;
  static const double small = 4.0;
  static const double medium = 8.0;
  static const double large = 16.0;
  static const double extraLarge = 24.0;
  static const double huge = 32.0;
  static const double massive = 48.0;

  // Screen padding
  static const double screenPadding = md;
  static const double screenPaddingHorizontal = md;
  static const double screenPaddingVertical = lg;

  // Card spacing
  static const double cardPadding = md;
  static const double cardMargin = sm;
  static const double cardSpacing = md;

  // List spacing
  static const double listItemPadding = md;
  static const double listItemSpacing = sm;
  static const double listSectionSpacing = lg;

  // Button spacing
  static const double buttonPadding = md;
  static const double buttonMargin = sm;
  static const double buttonSpacing = md;

  // Form spacing
  static const double formFieldSpacing = md;
  static const double formSectionSpacing = lg;
  static const double formPadding = md;

  // AppBar spacing
  static const double appBarPadding = md;
  static const double appBarSpacing = sm;

  // Bottom navigation spacing
  static const double bottomNavPadding = sm;
  static const double bottomNavSpacing = xs;

  // Dialog spacing
  static const double dialogPadding = lg;
  static const double dialogSpacing = md;
  static const double dialogMargin = lg;

  // Snackbar spacing
  static const double snackbarPadding = md;
  static const double snackbarMargin = sm;

  // Tab spacing
  static const double tabPadding = md;
  static const double tabSpacing = sm;

  // Chip spacing
  static const double chipPadding = sm;
  static const double chipSpacing = xs;
  static const double chipMargin = xs;

  // Icon spacing
  static const double iconPadding = sm;
  static const double iconSpacing = xs;
  static const double iconMargin = xs;

  // Divider spacing
  static const double dividerSpacing = md;
  static const double dividerMargin = sm;
} 